let average = 0;
let numVolumes = 0;

for (const user of users) {
	for (const userKeys in user) {
		if (userKeys === 'favoritesSounds') {
			for (const soundKey in user[userKeys]) {
				average += user[userKeys][soundKey].volume;
				numVolumes++;
				console.log(average);
			}
		}
	}
}

average /= numVolumes;

